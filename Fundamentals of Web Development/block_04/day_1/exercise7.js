let value = 100;

if (value < 0 || value > 100) {
  console.log(value);
} else if (value >= 90) {
  console.log("A");
} else if (value >= 80) {
  console.log("B");
} else if (value >= 70) {
  console.log("C");
} else if (value >= 60) {
  console.log("D");
} else if (value >= 50) {
  console.log("E");
} else if (value < 50) {
  console.log("F");
}
